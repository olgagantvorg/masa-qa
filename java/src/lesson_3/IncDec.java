package lesson_3;

public class IncDec {
    public static void main(String[] args) {
        int a = 5;
        a += 5;
        System.out.println("a = " + a);
        a = a + 1;
        System.out.println("a = " + a);
        a += 1;
        System.out.println("a = " + a);
        a++;
        System.out.println("a = " + a);
        a--;
        System.out.println("a = " + a);

    }
}
